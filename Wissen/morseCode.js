decodeMorse = function(morseCode) {
    //your code here
    let arrMorseCode = morseCode.trim().split("   ");
    let result = "";
    arrMorseCode.forEach((el, idx) => {
        let arrLetter = el.split(" ");
        arrLetter.forEach(letter => {
            result += MORSE_CODE[letter]
        });
        if (idx != arrMorseCode.length - 1) {
            result += " "
        }
    });

    return result;
}

